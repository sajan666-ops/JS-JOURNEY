// so here i learned about MATHS in JAVASCRIPT.

console.log(Math) // so this is used to output the simple maths function
console.log(Math.abs(-33)) // this is absolute. it is used to convert the negative number to positive 
console.log(Math.round(4.3)) // this is use the round off the decimal number
console.log(Math.ceil(6.1)) // it is always used to round to round off to bigger side.
console.log(Math.floor(9.9)) // it is always used to round to round off to smaller side.
console.log(Math.min(4,7,2,8)) // it is used to find the minimum of all the numbers
console.log(Math.max(4,7,2,8)) // it is used to find the maximum of all the numbers
console.log(Math.random()) // it is used to get the random number. these numbers are very low 
console.log(Math.random()*10 + 1) // by this we can get the number which will be start from one

let max = 20 // here we assign the max number 
let min = 10 // here we assign the min number 

console.log(Math.floor(Math.random() * max - min + 1) + min) // by this we can get the random number in bw MIN and MAX.

